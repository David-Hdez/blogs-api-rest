    {
        //
        $post = Post::find($id)->load('category');

        if (is_object($post)) {
            $resp=array(
                'code'=>200,
                'post'=>$post
            );
        } else {
            $resp=array(
                'code'=>404,
                'message'=>'Post does not exists'
            );
        }
        

        return response()->json($resp, $resp['code']);
    }

